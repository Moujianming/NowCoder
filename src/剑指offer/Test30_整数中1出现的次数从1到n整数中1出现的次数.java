public class Test30_整数中1出现的次数从1到n整数中1出现的次数 {
	 public int NumberOf1Between1AndN_Solution(int n) {
		    int result = 0;
		 for(int i = 1;i<=n;i++)
		 {
			 if(Judge(i)>0)
				 {
				 result+=Judge(i);
				 //System.out.println(i);
				 }
		 }
		 return result;
	    }
	 public int Judge(int n)
	 {
		 int res = 0;
		 while(n/10!=0)
		 {
			 if(n%10==1)res++;
			 n = n/10;	 
		 }
		 if(n%10==1)res++;
		 return res;
	 }
	public static void main(String[] args) {
		System.out.println(new Test30_整数中1出现的次数从1到n整数中1出现的次数().NumberOf1Between1AndN_Solution(1));
	}
}
